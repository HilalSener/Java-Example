    public Movies() throws Exception{
        initComponents();
        
        DBConnection db = new DBConnection();
        try {
            ResultSet sonuc = db.getMovies();

            while(sonuc.next())
            {
                int id = sonuc.getInt("id");
                String filmAd = sonuc.getString("filmAd");
                String tur = sonuc.getString("tur");
                int sure = sonuc.getInt("sure");
                int imdb = sonuc.getInt("imdb");
                Object[] resultList = { id, filmAd, tur, sure, imdb };
                
                //System.out.println(Arrays.toString(resultList));
                dtm.addRow(resultList);
            }
        } catch (Exception e) { e.printStackTrace(); }
    }
    
